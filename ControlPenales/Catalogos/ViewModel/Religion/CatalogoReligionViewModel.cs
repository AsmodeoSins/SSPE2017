    partial class CatalogoReligionViewModel : ValidationViewModelBase, IPageViewModel, IDataErrorInfo
    {
        public CatalogoReligionViewModel()
        {
            EmptyVisible = false;
            //Listado 
            ListItems = new List<RELIGION>();
            CatalogoHeader = "Religión";
            HeaderAgregar = "Agregar Religión";
